#ifndef MESH_H
#define MESH_H

#include "vao.h"
#include "vbo.h"
#include "ebo.h"
#include "texture.h"
#include "camera.h"

typedef struct {
    Vertices *vertices;
    GLuint *indices;
    GLuint *textureIDs;
    size_t indicesSize;
    GLuint VAO;
    GLuint VBO;
    GLuint EBO;
    GLuint nTextures;
} Mesh;

int MESH_initialize(Mesh *mesh, Vertices *verticesRef, GLuint *indices, GLuint *textureIDs, size_t indicesSize, int nTextures);
int MESH_draw(Mesh *mesh, GLuint shaderID, Camera *camera);
int MESH_delete(Mesh *mesh);

#endif
